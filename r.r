# We can use the print() function
print("Abishaik !")
#Output:[1] "Abishaik !"


# Quotes can be suppressed in the output
print("Abishaik !", quote = FALSE)
#Output:[1] Abishaik !


# If there are more than 1 item, we can concatenate using paste()
print(paste("How","are","you?"))
#Output:[1] "How are you?"